if (!function_exists('peakrackRiskAdminLanguage')) {
    function peakrackRiskAdminLanguage(array $settings): string
    {
        $language = (string) ($settings['adminLanguage'] ?? 'en');
        return in_array($language, ['en', 'zh'], true) ? $language : 'en';
    }
}

